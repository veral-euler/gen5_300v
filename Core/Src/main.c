void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
	if (htim->Instance == TIM17) {
		d.encoder_count = __HAL_TIM_GET_COUNTER(&htim2);
		d.mech_angle = ((float)d.encoder_count / 4096.0f) * 2.0f * M_PI;
		d.mech_angle = fmodf(d.mech_angle, (2.0f * M_PI));
		d.elec_angle = d.mech_angle * d.pole_pair;
		d.elec_angle = fmodf(d.elec_angle, (2.0f * M_PI));

		d.Va = d.Vrms * sinf(d.elec_angle);
		d.Vb = d.Vrms * sinf(d.elec_angle + 2.094f);
		d.Vc = d.Vrms * sinf(d.elec_angle - 2.094f);

		volatile float max_v = fmaxf(d.Va, fmaxf(d.Vb, d.Vc));
		volatile float min_v = fminf(d.Va, fminf(d.Vb, d.Vc));

		volatile float v_off = (max_v + min_v) / 2.0f;

		d.Va_SVM = d.Va - v_off;
		d.Va_SVM *= 1.15;

		d.Vb_SVM = d.Vb - v_off;
		d.Vb_SVM *= 1.15;

		d.Vc_SVM = d.Vc - v_off;
		d.Vc_SVM *= 1.15;

		d.pwm_a = (uint16_t)((d.Va_SVM / d.Vmax_SVM) * 1250.0f + 1250.0f);
		d.pwm_b = (uint16_t)((d.Vb_SVM / d.Vmax_SVM) * 1250.0f + 1250.0f);
		d.pwm_c = (uint16_t)((d.Vc_SVM / d.Vmax_SVM) * 1250.0f + 1250.0f);

		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_3, d.pwm_a);
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, d.pwm_b);
		__HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, d.pwm_c);
	}
}

